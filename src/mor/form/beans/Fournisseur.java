package mor.form.beans;

public class Fournisseur {
	
	private int id;
	private String nom;
	private String typeF;
	private String adresseF;
	private String mailF;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getTypeF() {
		return typeF;
	}
	public void setTypeF(String typeF) {
		this.typeF = typeF;
	}
	public String getAdresseF() {
		return adresseF;
	}
	public void setAdresseF(String adresseF) {
		this.adresseF = adresseF;
	}
	public String getMailF() {
		return mailF;
	}
	public void setMailF(String mailF) {
		this.mailF = mailF;
	}
	

}
